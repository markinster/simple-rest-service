package com.markinster.services.models;

public class Color {

	private String id;
	private String name;

	public Color() {

	}

	public Color(String id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setNome(String name) {
		this.name = name;
	}

}
